CREATE OR REPLACE TABLE RAW.RAW_TICKER_DETAILS (
	-- Symbol identification
	TICKER VARCHAR(16777216),
	SUFFIX VARCHAR(16777216),

	YF_TICKER VARCHAR(16777216),
		YF_SUFFIX VARCHAR(16777216),
	
	-- Geographic mapping
	COUNTRY_NAME VARCHAR(16777216),
	CONTINENT VARCHAR(16777216),
	
	-- Yahoo Finance details
	SHORTNAME VARCHAR(16777216),
	LONGNAME VARCHAR(16777216),
	QUOTETYPE VARCHAR(16777216),
	SECTOR VARCHAR(16777216),
	INDUSTRY VARCHAR(16777216),
	CURRENCY VARCHAR(16777216),
	EXCHANGE VARCHAR(16777216),
	
	-- Metadata
	INFO_FETCH_DATE DATE,
	SOURCE_SYSTEM VARCHAR(16777216) DEFAULT 'yahoo_finance',
	LAST_UPDATED TIMESTAMP_NTZ(9),
	LOAD_TS TIMESTAMP_NTZ(9) DEFAULT CURRENT_TIMESTAMP()
);